function WhatIsAnxiety() {
  return (
    <div className="space-y-6">
      <Section title="不安は、体の「お守り」かもしれない">
        <P>
          不安を感じるとき、胸がドキドキしたり、手に汗をかいたり、息が苦しくなったりすることがあります。
          これは体が「何か危ないことがあるかもしれない」と感じて、あなたを守ろうとしている反応だと言われています。
        </P>
        <P>
          脳の奥にある「扁桃体」という部分が、いわば火災報知器のような役割を果たしていて、
          危険を察知するとアラームを鳴らします。不安が強いときは、この火災報知器が少し敏感になっている状態——
          トーストを焼いただけで鳴ってしまうような感じ、と表現されることもあります。
        </P>
      </Section>

      <Section title="しんどいのは、本当のこと">
        <P>
          「気のせいだよ」「考えすぎだよ」と言われたことがあるかもしれません。
          でも、体に起きている反応は本物ですし、それがつらいのも事実です。
          不安は「壊れた」のではなく、体の仕組みが少し過敏に働いている状態です。
          あなたのせいではありません。
        </P>
        <Callout>
          動悸、発汗、息苦しさ、手の震え——こうした体の反応そのものは、危険ではないと言われています。
          ただ、とてもつらいときは無理をせず、主治医に相談してください。
        </Callout>
      </Section>

      <Section title="「治す」より「付き合い方を見つける」">
        <P>
          不安を完全にゼロにすることは、あまり現実的ではないかもしれません。
          なぜなら、不安は本来あなたを守るための仕組みだからです。
          大切なのは、不安とうまく付き合う方法を、少しずつ見つけていくことだと考えられています。
        </P>
        <P>
          たとえば、呼吸法で体の緊張をゆるめたり、認知行動療法（CBT）で思考のパターンに気づいたり。
          合う方法は人それぞれです。焦らず、自分に合うものを探していくプロセスそのものが、
          大切な一歩になります。
        </P>
      </Section>

      <Section title="助けを求めることは、賢い選択">
        <P>
          「こんなことで相談していいのかな」と思うかもしれません。
          でも、不安がつらいと感じているなら、それは十分に相談する理由になります。
          専門家に話すことは「弱さ」ではなく、自分を大切にする行動です。
        </P>
        <P>
          一人で抱えている時間が長くなるほど、しんどさは増していくことがあります。
          話してみようかな、と少しでも思ったら、その気持ちを大事にしてください。
        </P>
      </Section>

      <Disclaimer />
    </div>
  );
}

