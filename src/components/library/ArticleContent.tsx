function AboutMedication() {
  return (
    <div className="space-y-6">
      <Section title="薬は「道具」のひとつ">
        <P>
          薬を飲むことに抵抗を感じる人は少なくありません。
          「薬に頼るなんて」「自分の力で乗り越えなきゃ」——そう思うのは自然なことです。
        </P>
        <P>
          でも、たとえば足を骨折したとき、松葉杖を使うことを「弱い」とは思わないのではないでしょうか。
          心の薬も同じように、必要なときに使う道具のひとつだと考えられています。
          使うことは恥ずかしいことでも、弱いことでもありません。
        </P>
      </Section>

      <Section title="飲むことも、迷うことも、自然なこと">
        <P>
          「本当に効いているのかな」「飲まなくても大丈夫なんじゃないか」——
          薬について迷いが出るのは、ごく自然なことです。
          特に、体調が良くなってくると「もう必要ないかも」と感じることがあります。
        </P>
        <Callout>
          薬を飲み続けるかどうか迷ったら、自己判断でやめるのではなく、
          主治医に「迷っている」と正直に伝えてみてください。
          一緒に今後のことを考えてくれるはずです。
        </Callout>
      </Section>

      <Section title="副作用の不安は、伝えていい">
        <P>
          薬には副作用がある場合があります。眠気、だるさ、体重の変化、口の渇きなど、
          種類や体質によってさまざまです。
          「これくらいは我慢すべき」と思わなくて大丈夫です。
        </P>
        <P>
          副作用がつらいと感じたら、遠慮せず主治医に伝えてください。
          薬の種類や量を調整してもらえることがあります。
          我慢を続けることが治療の一部ではありません。
        </P>
      </Section>

      <Section title="「やめたい」と思うこと">
        <P>
          「もう薬を飲みたくない」と思うことがあるかもしれません。
          長く飲み続けることへの疲れ、「薬を飲んでいる自分」への複雑な気持ち——
          そうした感情は、あなただけではなく、多くの人が経験するものです。
        </P>
        <P>
          大切なのは、やめるときも主治医と一緒に決めることです。
          急にやめてしまうと、体や気持ちに大きな変化が起きることがあると言われています。
          「やめたい」と思ったことを主治医に話してみること——それ自体が大切なコミュニケーションです。
        </P>
      </Section>

      <Section title="飲んでも飲まなくても、あなたはあなた">
        <P>
          薬を飲んでいるからダメ、飲まなくなったからエライ——そういうものではありません。
          今の自分に必要なものを、必要なだけ使うこと。それだけです。
        </P>
        <P>
          薬とどう付き合うかは、あなたと主治医が一緒に考えていくことです。
          迷いがあるなら、それを抱えたまま相談しに行って大丈夫です。
        </P>
      </Section>

      <Disclaimer />
    </div>
  );
}

