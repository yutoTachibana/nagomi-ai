function CognitiveDistortions() {
  return (
    <div className="space-y-6">
      <Section title="思考の「クセ」に気づくということ">
        <P>
          私たちの頭の中では、毎日たくさんの考えが浮かんでいます。
          でも、その考えがいつも正確だとは限りません。
          疲れているとき、落ち込んでいるとき、不安なとき——
          思考には独特の「クセ」が出やすくなると言われています。
        </P>
        <P>
          認知行動療法（CBT）では、こうした思考のクセを「認知の歪み」と呼んでいます。
          歪みに気づくことは、自分を責めるためではなく、
          「ああ、今このパターンにはまっているな」と少し距離を置くためのものです。
        </P>
        <Callout>
          以下の12のパターンを読んで、「あ、これ私かも」と思うものがあるかもしれません。
          それは「ダメ」ということではなく、「気づけた」ということです。
        </Callout>
      </Section>

      <section className="space-y-4">
        {COGNITIVE_DISTORTIONS.map((d, i) => (
          <Card key={d.key} className="space-y-2">
            <p className="text-kana text-muted">{i + 1} / {COGNITIVE_DISTORTIONS.length}</p>
            <h3 className="font-mincho text-h3 leading-snug">{d.name}</h3>
            <p className="text-body leading-relaxed text-ink/90">{d.description}</p>
            <p className="text-body text-ink/70 italic">{d.example}</p>
            {GENTLE_NOTES[d.key] && (
              <div className="mt-2 pt-2 border-t border-accent-soft">
                <p className="text-small leading-relaxed text-sage">
                  {GENTLE_NOTES[d.key]}
                </p>
              </div>
            )}
          </Card>
        ))}
      </section>

      <Section title="気づくだけで、十分">
        <P>
          12のパターンを見て、「自分にはたくさん当てはまる」と感じたかもしれません。
          でも、これらは多くの人が持っている普通の思考パターンです。
          完全になくすことが目標ではなく、「あ、今この考え方をしているな」と気づけることが大切です。
        </P>
        <P>
          気づいたあと、すぐに考えを変えられなくても大丈夫です。
          気づくことそのものが、思考と少し距離を置くことにつながります。
        </P>
      </Section>

      <Disclaimer />
    </div>
  );
}

