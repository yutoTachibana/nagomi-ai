function Section({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <section className="space-y-3">
      <h2 className="font-mincho text-h2 leading-snug">{title}</h2>
      {children}
    </section>
  );
}

function P({ children }: { children: React.ReactNode }) {
  return <p className="text-body leading-relaxed text-ink/90">{children}</p>;
}

function Callout({ children }: { children: React.ReactNode }) {
  return (
    <Card warm className="my-4">
      <p className="text-body leading-relaxed text-ink/85">{children}</p>
    </Card>
  );
}

function Disclaimer() {
  return (
    <div className="mt-8 pt-6 border-t border-accent-soft">
      <p className="text-small text-muted leading-relaxed">
        この記事は情報提供を目的としており、医療上のアドバイスではありません。
        気になることがあれば、主治医や専門家に相談してください。
        あなたのペースで、必要なときに読み返してもらえたら嬉しいです。
      </p>
    </div>
  );
}

